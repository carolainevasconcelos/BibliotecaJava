    private void jBusuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBusuarioActionPerformed
        try {
        // Capturando os dados digitados pelo usu치rio
        String nome = jTnome.getText();
        String cpf = jTcpf.getText();
        String email = jTemail.getText();

        // Criando objeto da classe Usuario e atribuindo os valores
        Usuario usuario = new Usuario();
        usuario.setNome(nome);
        usuario.setCpf(cpf);
        usuario.setEmail(email);

        // Salvando no banco de dados
        usuario.salvar();

        // Mensagem de sucesso
        javax.swing.JOptionPane.showMessageDialog(this, "Usu치rio salvo com sucesso!");

        // Limpando os campos
        jTnome.setText("");
        jTcpf.setText("");
        jTemail.setText("");
    } catch (Exception e) {
        // Mensagem de erro
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao salvar usu치rio: " + e.getMessage());
    }
    }//GEN-LAST:event_jBusuarioActionPerformed

